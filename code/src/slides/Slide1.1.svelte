    <h1>What is the MBTA Communities Act?</h1>
    <h3>
        The MBTA Communities Act is a 2021 Massachusetts state law that aims to reduce the housing crisis by requiring cities near MBTA stations to upzone at least one area within their borders. The law requires that 177 Massachusetts municipalities designated as MBTA communities “shall have a zoning ordinance or bylaw that provides for at least 1 district of reasonable size” that permits multi-family housing as of right, meaning that multifamily residential development will be allowed there without an extensive permitting process. Massachusetts governor Maura Healy clarified that compliance with the law is mandatory and that noncompliance would not only result in a loss of state funding for housing but that the state of Massachusetts might also bring legal action to communities that did not comply. <sup id="fnref1"><a href="#fn1">[1]</a></sup> <br> <br>
            Though the law was passed with broad bipartisan support, it remains controversial in some areas, particularly in lower-density, suburban communities served by MBTA Commuter Rail. As of April 2024, all 177 communities have submitted appropriate zoning proposals to the state and thus are technically compliant with the law, but many, including Holden, Rockford, and Bourne, raised significant legal challenges along the way, and whether all communities will become compliant remains to be seen. On the other hand, some communities, such as Brookline, have embraced the legislation and have passed new zoning regulations that go above and beyond the law’s requirements. <br> <br>
            Many critics of the act have claimed that upzoning these suburban communities would significantly change the urban form of low-density communities in culturally incompatible ways. It is essential to understand that the MBTA Communities Act does not directly dictate what type of housing is built in these communities; it simply requires a single district within the municipality where some form of multifamily housing is allowed. This gives local governments significant influence on the location and nature of their multifamily districts. <br> <br>
            Use this interactive map to explore the potential effects of siting multifamily districts near different transit stations within communities. In addition to providing detailed parcel-level information about current zoning and usage patterns and how these might change if upzoning were to occur, the tool provides relevant demographic statistics about the area surrounding each MBTA station.
        </h3>
